		<p>Stewart Donaldson is a forty-seven year old recruiter for a software company. He likes his Macbook Pro and is
			skeptical of new talent but has a good eye for spotting true professionalism in candidates. He is currently
			seeking a junior developer to join the team and came across a link to Max's online portfolio through LinkedIn.
			His purpose in visiting Max's site is to gage his skillset and experience in relation to the available position
			in order to know whether or not to contact Max for an interview. Stewart is tired of seeing lacklustre
			portfolios and resumes. He is tired of having to try to navigate through a less than ideal user experience. He
			is pleased with the look and function of Max's portfolio. Stewart will need references upon request and copies
			of any certifications if he decides on interviewing Max. </p>
		<h2>Use Case</h2>
		<p>Stewart has come across Max's portfolio at about 10AM in the morning while he is sitting at his desk at work.
			He has just finished his first cup of coffee and it has somehow already started to wear off. He is tired of
			sifting through inefficiently designed online portfolios and slapped together resumes. He is looking for
			someone young yet skilled who is able to show you everything you need to know without over-saturating. He is
			using his relatively new Macbook Pro to access the site and already likes the "smart look" that Max's portfolio
			gives off. It is a site that is refreshing to come across in today's advertisement filled internet. He rarely
			uses his mobile device to browse sites like Max's but will do it from time to time on his lunch break, which is
			why a portfolio that is mobile friendly is also important to him and a factor in his decision process for a new
			hire.</p>


